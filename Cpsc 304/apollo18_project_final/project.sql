drop table music_user cascade constraints;
drop table listener cascade constraints;
drop table artist cascade constraints;
drop table audio cascade constraints;
drop table album cascade constraints;
drop table playlist cascade constraints;
drop table creates cascade constraints;
drop table contains cascade constraints;
drop table rates cascade constraints;

create table music_user(
  userid integer not null,
      user_name varchar(40) unique,
  user_join_year integer,
	primary key (userid)
  );
create table artist(
  userid integer not null,
  record_label varchar(40),
  artist_genre varchar(40),
	primary key (userid),
  foreign key (userid) references music_user on delete cascade
	);
create table album(
  aid integer not null,
  album_name varchar(40) unique,
  album_year integer,
  userid integer not null,
  check (album_year >= 0 and album_year <= 9999),
	primary key (aid),
  foreign key (userid) references music_user on delete cascade
	);
create table audio(
  sid integer not null,
  aid integer,
  audio_name varchar(40) unique,
  audio_year integer,
  audio_genre varchar(40),
  audio_length integer,
  audio_type varchar(10),
	primary key (sid),
  foreign key (aid) references album,
  check (audio_year >= 0 and audio_year <= 9999 and audio_length >= 0)
	);
create table listener(
  userid integer not null,
  listener_sid integer,
  primary key (userid),
  foreign key (userid) references music_user,
  foreign key (listener_sid) references audio
	);
create table playlist(
  lid integer not null,
  list_name varchar(40),
  userid integer not null,
	primary key (userid, lid),
  foreign key (userid) references listener on delete cascade
	);
create table creates(
  userid integer not null,
  sid integer not null,
	primary key (userid,sid),
  foreign key (sid) references audio,
  foreign key (userid) references music_user on delete cascade
	);
create table contains(
  lid integer not null,
  sid integer not null,
  userid integer not null,
	primary key (lid,sid),
  foreign key (sid) references audio,
  foreign key (userid, lid) references playlist,
  foreign key (userid) references listener
	);
create table rates(
  userid integer not null,
  sid integer not null,
  rating integer,
	primary key (userid,sid),
  foreign key (sid) references audio,
  foreign key (userid) references listener,
  check(rating >= 1 and rating <= 5)
  );

alter session set NLS_DATE_FORMAT='DD-MON-YYYY';
commit;

